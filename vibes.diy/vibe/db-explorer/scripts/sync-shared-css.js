#!/usr/bin/env node

/**
 * Sync the db-explorer base source CSS into the generated explorer asset.
 *
 * The source file stays intentionally narrow and only imports the shared host
 * tokens and base reset rules that db-explorer consumes. Local imports are
 * flattened so the generated asset is plain browser-consumable CSS.
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const SOURCE_PATH = path.resolve(
  __dirname,
  "../db-explorer-base.css",
);
const DESTINATION_PATH = path.resolve(
  __dirname,
  "../public/db-explorer-base.css",
);
const SHOULD_CHECK = process.argv.includes("--check");
const IMPORT_REGEX = /^\s*@import\s+"([^"]+)";\s*$/gm;

function resolveCssImports(filePath, seen = new Set()) {
  const resolvedPath = path.resolve(filePath);
  if (seen.has(resolvedPath)) {
    return "";
  }

  seen.add(resolvedPath);

  const sourceCss = fs.readFileSync(resolvedPath, "utf-8");
  return sourceCss.replace(IMPORT_REGEX, (fullMatch, importPath) => {
    if (!importPath.startsWith(".")) {
      return "";
    }

    const importedPath = path.resolve(path.dirname(resolvedPath), importPath);
    return `${resolveCssImports(importedPath, seen)}\n`;
  });
}

function stripTailwindOnlyDirectives(sourceCss) {
  const withoutThemeBlocks = sourceCss.replace(/@theme\s*\{[\s\S]*?\n\}/g, "");
  return withoutThemeBlocks.replace(/^\s*@import\s+"tailwindcss";\s*$/gm, "");
}

function buildGeneratedCss() {
  const sourceCss = stripTailwindOnlyDirectives(
    resolveCssImports(SOURCE_PATH),
  ).trim();

  return `/**
 * Generated from ../db-explorer-base.css
 * Do not edit this file directly.
 */

${sourceCss}`;
}

function readCurrentCss() {
  try {
    return fs.readFileSync(DESTINATION_PATH, "utf-8");
  } catch (error) {
    if (
      error &&
      typeof error === "object" &&
      "code" in error &&
      error.code === "ENOENT"
    ) {
      return "";
    }

    throw error;
  }
}

function checkCss() {
  const generatedCss = buildGeneratedCss();
  const currentCss = readCurrentCss();

  if (currentCss !== generatedCss) {
    console.error(
      "db-explorer shared CSS is out of sync. Run `pnpm --filter @vibes.diy/vibe-db-explorer sync:css`.",
    );
    process.exit(1);
  }

  console.log("db-explorer shared CSS is up to date.");
}

function syncCss() {
  const generatedCss = buildGeneratedCss();
  const currentCss = readCurrentCss();

  if (currentCss !== generatedCss) {
    fs.writeFileSync(DESTINATION_PATH, generatedCss);
    console.log(
      `Updated ${path.relative(process.cwd(), DESTINATION_PATH)} from db-explorer base source CSS.`,
    );
    return;
  }

  console.log("db-explorer shared CSS already up to date.");
}

if (SHOULD_CHECK) {
  checkCss();
} else {
  syncCss();
}
